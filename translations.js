        // Update app page CTA
        updateElement('.cta__title', t.app.cta.title);
        updateElement('.cta__description', t.app.cta.description);
        updateElement('.cta__button', t.app.cta.button);
        
        // Update app page modal
        updateElement('.modal__header h3', t.app.modal.title);
